package classemusic;




public class Mae {
    String nome;
   String pais;
   String ano;

    public Mae(String nome, String pais, String ano) {
        this.nome = nome;
        this.pais = pais;
        this.ano = ano;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getPais() {
        return pais;
    }

    public void setPais(String pais) {
        this.pais = pais;
    }

    public String getAno() {
        return ano;
    }

    public void setAno(String ano) {
        this.ano = ano;
    }
   
}
   